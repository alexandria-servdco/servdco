/**
 * Download US city/ZIP reference data and generate a Supabase migration.
 *
 * Source: SimpleMaps-format US ZIP CSV (public GitHub mirror).
 * Run: node scripts/generate-full-us-geo-migration.mjs
 *
 * Optional: place CSV at data/uszips-source.csv to skip download.
 */
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");

const CSV_URL =
  "https://raw.githubusercontent.com/akinniyi/US-Zip-Codes-With-City-State/master/uszips.csv";

const LOCAL_CSV = resolve(root, "data/uszips-source.csv");

/** US states + DC — excludes territories (PR, GU, etc.) */
const US_STATE_CODES = new Set([
  "AL", "AK", "AZ", "AR", "CA", "CO", "CT", "DE", "DC", "FL",
  "GA", "HI", "ID", "IL", "IN", "IA", "KS", "KY", "LA", "ME",
  "MD", "MA", "MI", "MN", "MS", "MO", "MT", "NE", "NV", "NH",
  "NJ", "NM", "NY", "NC", "ND", "OH", "OK", "OR", "PA", "RI",
  "SC", "SD", "TN", "TX", "UT", "VT", "VA", "WA", "WV", "WI", "WY",
]);

function normalizeCity(city) {
  return city.trim().replace(/\s+/g, " ").toLowerCase();
}

function escapeSql(value) {
  return value.replace(/'/g, "''");
}

function parseCsvLine(line) {
  const fields = [];
  let current = "";
  let inQuotes = false;

  for (let i = 0; i < line.length; i++) {
    const ch = line[i];
    if (ch === '"') {
      if (inQuotes && line[i + 1] === '"') {
        current += '"';
        i++;
      } else {
        inQuotes = !inQuotes;
      }
    } else if (ch === "," && !inQuotes) {
      fields.push(current);
      current = "";
    } else {
      current += ch;
    }
  }
  fields.push(current);
  return fields.map((f) => f.trim().replace(/^"|"$/g, ""));
}

async function loadCsv() {
  if (existsSync(LOCAL_CSV)) {
    console.log(`Using cached CSV: ${LOCAL_CSV}`);
    return readFileSync(LOCAL_CSV, "utf8");
  }

  console.log("Downloading US ZIP dataset…");
  const res = await fetch(CSV_URL, {
    headers: { "User-Agent": "servdco-geo-import/1.0" },
  });
  if (!res.ok) {
    throw new Error(`Download failed: ${res.status} ${res.statusText}`);
  }
  const text = await res.text();
  writeFileSync(LOCAL_CSV, text, "utf8");
  return text;
}

function parseRows(csvText) {
  const lines = csvText.split(/\r?\n/).filter((l) => l.trim());
  if (lines.length < 2) throw new Error("CSV empty");

  const header = parseCsvLine(lines[0]).map((h) => h.toLowerCase());
  const zipIdx = header.indexOf("zip");
  const cityIdx = header.indexOf("city");
  const stateIdx = header.findIndex(
    (h) => h === "state_id" || h === "state_code" || h === "state",
  );

  if (zipIdx < 0 || cityIdx < 0 || stateIdx < 0) {
    throw new Error(`Unexpected CSV columns: ${header.join(", ")}`);
  }

  const seen = new Set();
  const rows = [];

  for (const line of lines.slice(1)) {
    const cols = parseCsvLine(line);
    const zip = (cols[zipIdx] ?? "").replace(/\D/g, "").slice(0, 5);
    const cityName = (cols[cityIdx] ?? "").trim();
    const stateCode = (cols[stateIdx] ?? "").trim().toUpperCase().slice(0, 2);

    if (!/^\d{5}$/.test(zip) || !US_STATE_CODES.has(stateCode) || !cityName) {
      continue;
    }

    const city_normalized = normalizeCity(cityName);
    const key = `${stateCode}|${city_normalized}|${zip}`;
    if (seen.has(key)) continue;
    seen.add(key);

    rows.push({ stateCode, cityName, city_normalized, zip });
  }

  rows.sort((a, b) =>
    a.stateCode.localeCompare(b.stateCode) ||
    a.cityName.localeCompare(b.cityName) ||
    a.zip.localeCompare(b.zip),
  );

  return rows;
}

function buildMigration(rows) {
  const BATCH = 400;
  const chunks = [];

  for (let i = 0; i < rows.length; i += BATCH) {
    const batch = rows.slice(i, i + BATCH);
    const values = batch
      .map(
        (r) =>
          `('${escapeSql(r.stateCode)}', '${escapeSql(r.cityName)}', '${escapeSql(r.city_normalized)}', '${escapeSql(r.zip)}')`,
      )
      .join(",\n  ");
    chunks.push(
      `INSERT INTO public.geo_city_zip_codes (state_code, city_name, city_normalized, zip_code)\nVALUES\n  ${values}\nON CONFLICT (state_code, city_normalized, zip_code) DO NOTHING;`,
    );
  }

  return `-- Full US city/ZIP reference data (50 states + DC)
-- Generated by scripts/generate-full-us-geo-migration.mjs — ${rows.length} unique rows
-- Source: SimpleMaps-format US ZIP CSV (uszips.csv)
-- Re-run script to refresh; ON CONFLICT keeps existing rows.

${chunks.join("\n\n")}
`;
}

async function main() {
  const csv = await loadCsv();
  console.log(`CSV size: ${(csv.length / 1024 / 1024).toFixed(2)} MB`);

  const rows = parseRows(csv);
  console.log(`Parsed ${rows.length} unique state/city/zip rows`);

  const ohCount = rows.filter((r) => r.stateCode === "OH").length;
  const has43004 = rows.some((r) => r.zip === "43004");
  console.log(`Ohio rows: ${ohCount}, includes 43004: ${has43004}`);

  const sql = buildMigration(rows);
  const outPath = resolve(
    root,
    "supabase/migrations/20250701120000_full_us_geo_zips.sql",
  );
  writeFileSync(outPath, sql, "utf8");
  console.log(`Wrote ${outPath} (${(sql.length / 1024 / 1024).toFixed(2)} MB)`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
