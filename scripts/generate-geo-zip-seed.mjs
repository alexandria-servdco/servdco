/**
 * Generates geo_city_zip_codes seed SQL from the legacy TS dataset.
 * Run: node scripts/generate-geo-zip-seed.mjs
 */
import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");

function normalizeCity(city) {
  return city.trim().replace(/\s+/g, " ").toLowerCase();
}

function escapeSql(value) {
  return value.replace(/'/g, "''");
}

const src = readFileSync(
  resolve(root, "client/lib/zip-codes-by-city-data.ts"),
  "utf8",
);

const dataMatch = src.match(
  /export const ZIP_CODES_BY_CITY[^=]*=\s*(\{[\s\S]*?\n\});/,
);
if (!dataMatch) {
  throw new Error("Could not parse ZIP_CODES_BY_CITY from zip-codes-by-city.ts");
}

const dataset = eval(`(${dataMatch[1]})`);
const rows = [];

for (const [stateCode, cities] of Object.entries(dataset)) {
  for (const [cityName, zips] of Object.entries(cities)) {
    const normalized = normalizeCity(cityName);
    for (const zip of zips) {
      rows.push({
        stateCode: stateCode.toUpperCase(),
        cityName,
        normalized,
        zip,
      });
    }
  }
}

const values = rows
  .map(
    (r) =>
      `('${escapeSql(r.stateCode)}', '${escapeSql(r.cityName)}', '${escapeSql(r.normalized)}', '${escapeSql(r.zip)}')`,
  )
  .join(",\n  ");

const sql = `-- Auto-generated by scripts/generate-geo-zip-seed.mjs — ${rows.length} rows
-- Re-run script after updating client/lib/zip-codes-by-city.ts or import CSV via scripts/import-geo-zip-csv.mjs

INSERT INTO public.geo_city_zip_codes (state_code, city_name, city_normalized, zip_code)
VALUES
  ${values}
ON CONFLICT (state_code, city_normalized, zip_code) DO NOTHING;
`;

const outPath = resolve(
  root,
  "supabase/migrations/20250623100001_geo_city_zip_seed.sql",
);
writeFileSync(outPath, sql, "utf8");
console.log(`Wrote ${rows.length} rows to ${outPath}`);
