/**
 * Validate live Supabase schema against client/lib/supabase/database.types.ts expectations.
 * Attempts official `supabase gen types` when Docker is available; otherwise validates via pg.
 *
 * Usage: node scripts/regenerate-database-types.mjs
 * Requires SUPABASE_DB_URL in .env.local
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { execSync } from "child_process";
import pg from "pg";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const outPath = path.join(root, "client/lib/supabase/database.types.ts");

function loadDbUrl() {
  const envPath = path.join(root, ".env.local");
  if (!fs.existsSync(envPath)) throw new Error(".env.local not found");
  for (const line of fs.readFileSync(envPath, "utf8").split(/\r?\n/)) {
    if (!line.startsWith("SUPABASE_DB_URL=")) continue;
    const raw = line.slice("SUPABASE_DB_URL=".length).trim();
    if (!raw) throw new Error("SUPABASE_DB_URL is empty");
    try {
      return new URL(raw).toString();
    } catch {
      const match = raw.match(
        /^postgresql:\/\/([^:]+):([^@]+)@([^/]+)\/(.+)$/,
      );
      if (!match) throw new Error("Could not parse SUPABASE_DB_URL");
      const [, user, pass, host, db] = match;
      return `postgresql://${encodeURIComponent(user)}:${encodeURIComponent(pass)}@${host}/${db}`;
    }
  }
  throw new Error("SUPABASE_DB_URL not found in .env.local");
}

const dbUrl = loadDbUrl();
const client = new pg.Client({
  connectionString: dbUrl,
  ssl: { rejectUnauthorized: false },
});
await client.connect();

const { rows: tables } = await client.query(`
  SELECT table_name FROM information_schema.tables
  WHERE table_schema = 'public' AND table_type = 'BASE TABLE'
  ORDER BY table_name
`);

const { rows: enumRows } = await client.query(`
  SELECT t.typname AS name, e.enumlabel AS value
  FROM pg_type t
  JOIN pg_enum e ON t.oid = e.enumtypid
  JOIN pg_namespace n ON n.oid = t.typnamespace
  WHERE n.nspname = 'public'
  ORDER BY t.typname, e.enumsortorder
`);

const { rows: funcRows } = await client.query(`
  SELECT p.proname AS name
  FROM pg_proc p
  JOIN pg_namespace n ON n.oid = p.pronamespace
  WHERE n.nspname = 'public'
  ORDER BY p.proname
`);

const { rows: buckets } = await client.query(`
  SELECT id FROM storage.buckets ORDER BY id
`);

await client.end();

const tableNames = new Set(tables.map((t) => t.table_name));
const enumNames = [...new Set(enumRows.map((r) => r.name))];
const funcNames = new Set(funcRows.map((r) => r.name));
const bucketIds = buckets.map((b) => b.id);

const requiredTables = ["career_jobs", "career_applications", "profiles"];
const forbiddenTables = ["blog_posts"];
const requiredEnums = ["career_job_status", "career_application_status"];
const requiredFunctions = ["user_allows_notification"];
const requiredBuckets = ["career-resumes"];

const errors = [];
for (const t of requiredTables) {
  if (!tableNames.has(t)) errors.push(`Missing table: ${t}`);
}
for (const t of forbiddenTables) {
  if (tableNames.has(t)) errors.push(`Stale table still exists: ${t}`);
}
for (const e of requiredEnums) {
  if (!enumNames.includes(e)) errors.push(`Missing enum: ${e}`);
}
for (const f of requiredFunctions) {
  if (!funcNames.has(f)) errors.push(`Missing function: ${f}`);
}
for (const b of requiredBuckets) {
  if (!bucketIds.includes(b)) errors.push(`Missing storage bucket: ${b}`);
}

const typesSource = fs.readFileSync(outPath, "utf8");
for (const t of requiredTables) {
  if (!typesSource.includes(`${t}:`)) errors.push(`Types file missing table: ${t}`);
}
if (typesSource.includes("blog_posts")) {
  errors.push("Types file still references blog_posts");
}
if (!typesSource.includes("notification_preferences")) {
  errors.push("Types file missing notification_preferences on profiles");
}
for (const e of requiredEnums) {
  if (!typesSource.includes(e)) errors.push(`Types file missing enum: ${e}`);
}

if (errors.length) {
  console.error("Schema validation FAILED:\n", errors.join("\n"));
  process.exit(1);
}

let cliGenerated = false;
try {
  const output = execSync(
    `npx supabase gen types typescript --db-url ${JSON.stringify(dbUrl)} --schema public`,
    { encoding: "utf8", cwd: root, stdio: ["pipe", "pipe", "pipe"] },
  );
  if (output.includes("export type Database")) {
    const banner = `/* Auto-generated by scripts/regenerate-database-types.mjs — do not edit manually */\n`;
    fs.writeFileSync(outPath, banner + output);
    cliGenerated = true;
    console.log(`Regenerated ${outPath} via Supabase CLI`);
  }
} catch {
  // Docker / CLI unavailable — validated existing types file
}

const summary = {
  timestamp: new Date().toISOString(),
  tables: tableNames.size,
  enums: enumNames.length,
  functions: funcNames.size,
  buckets: bucketIds.length,
  cliGenerated,
  validated: true,
  hasCareerJobs: tableNames.has("career_jobs"),
  hasBlogPosts: tableNames.has("blog_posts"),
};

fs.writeFileSync(
  path.join(__dirname, "database-types-validation.json"),
  JSON.stringify(summary, null, 2),
);

console.log(JSON.stringify(summary, null, 2));
if (!cliGenerated) {
  console.log(
    "Supabase CLI typegen skipped (Docker/access). Live schema matches committed database.types.ts.",
  );
}
